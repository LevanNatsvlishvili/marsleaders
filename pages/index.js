import Head from 'next/head';
import useStore from 'context';
import { useEffect, useState } from 'react';
import Welcome from 'components/Pages/Welcome';
import About from 'components/Pages/About';
import Project from 'components/Pages/Project';
import Timeline from 'components/Pages/Timeline';
import BenefitsComponent from 'components/Pages/Benefits/Benefits';
import Faq from 'components/Pages/Faq';
import { Swiper, SwiperSlide } from 'swiper/react';
import 'swiper/css';
import 'swiper/css/pagination';
import { Mousewheel, Pagination } from 'swiper';
import { Logo } from 'components/Icons/Logo';

export default function Home() {
  const { currView, setCurrView, setSwiperRef } = useStore();
  const [carousel, setCarousel] = useState(0);

  useEffect(() => {
    setTimeout(() => {
      if (carousel > 90) {
        setCarousel(0);
        return;
      }
      setCarousel(carousel - 1);
    }, 300);

    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [currView]);

  const handleSlideChange = (e) => {
    setCurrView(e.activeIndex);
  };

  const swiperSettigns = {
    mousewheel: {
      releaseOnEdges: true,
    },
    direction: 'vertical',
    slidesPerView: 1,
    mousewheel: {
      forceToAxis: true,
      sensitivity: 1,
      releaseOnEdges: true,
      thresholdDelta: 50,
    },
    pagination: {
      clickable: false,
    },
    onSlideChange: handleSlideChange,
    allowTouchMove: false,
    onSwiper: setSwiperRef,
  };

  return (
    <div className="w-full">
      <Head>
        <title>Marsleaders</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href={Logo.src} />
      </Head>

      <Swiper
        {...swiperSettigns}
        modules={[Mousewheel, Pagination]}
        className="mySwiper h-full"
      >
        <SwiperSlide className="!h-full">
          <Welcome carousel={carousel} />
        </SwiperSlide>
        <SwiperSlide className="!h-full">
          <About carousel={carousel} />
        </SwiperSlide>
        <SwiperSlide className="!h-full">
          <Project carousel={carousel} />
        </SwiperSlide>
        <SwiperSlide className="!h-full">
          <Timeline carousel={carousel} />
        </SwiperSlide>
        <SwiperSlide className="!h-full">
          <BenefitsComponent carousel={carousel} />
        </SwiperSlide>
        <SwiperSlide className="!h-full">
          <Faq carousel={carousel} />
        </SwiperSlide>
      </Swiper>

      <style jsx global>
        {`
          .swiper {
            width: 100%;
            height: 100%;
          }
        `}
      </style>
    </div>
  );
}
